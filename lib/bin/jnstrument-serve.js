#!/usr/bin/env node
const program = require('commander');
const server = require('../server');
///////////////////////////////////////////////////////////////////////////////

program
  .option('-p, --port <port>', 'port to serve app through')
  .option('-s, --source <source>', 'source file to visualize')
  .parse(process.argv);
  

server(program.source, program.port);
  
