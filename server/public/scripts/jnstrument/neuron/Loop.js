(function($, THREE, window, document, undefined) {	
	var Loop = function(){
/*/////////////////////////////////////////////////////////////////////////////
	Private Properties
/*/////////////////////////////////////////////////////////////////////////////
/*/////////////////////////////////////////////////////////////////////////////
	Private Methods
/*/////////////////////////////////////////////////////////////////////////////
		var loop = function(){
			window.requestAnimationFrame(loop.bind(this));
			this.emitEvent();
		}
/*/////////////////////////////////////////////////////////////////////////////
	Public Methods
/*/////////////////////////////////////////////////////////////////////////////
		this.construct = function(){
			loop.bind(this)();
			this.activate();
		}
	}
///////////////////////////////////////////////////////////////////////////////
	module.exports = require("./EventEmitter.js").extend(Loop);
})(jQuery, THREE, window, document)