(function($, THREE, window, document, undefined) {
	var EventPool = function(){
/*/////////////////////////////////////////////////////////////////////////////
	Private Properties
/*/////////////////////////////////////////////////////////////////////////////
		var elements = [];
		var env;
/*/////////////////////////////////////////////////////////////////////////////
	Public Properties
/*/////////////////////////////////////////////////////////////////////////////
/*/////////////////////////////////////////////////////////////////////////////
	Private Methods
/*/////////////////////////////////////////////////////////////////////////////
/*/////////////////////////////////////////////////////////////////////////////
	Public Methods
/*/////////////////////////////////////////////////////////////////////////////
		this.getElement = function(){
			for(var i=0; i<elements.length; i++)
				if(elements[i].isAvailable()) {
					elements[i].setUnavailable();
					return elements[i];
				}
			var newElement = new (require("./EventElement.js"))(env);
			this.addElement(newElement);
			return newElement;
		}
///////////////////////////////////////////////////////////////////////////////
		this.addElement = function(element){
			if(!element.instanceof(require("./poolable.js"))) throw("Only instances of poolable can be added to a pool.");
			elements.push(element);
		}
///////////////////////////////////////////////////////////////////////////////
		this.construct= function(_env){
			env = _env;
		}
	};
	module.exports = require("../Class.js")(EventPool);
})(jQuery, THREE, window, document)