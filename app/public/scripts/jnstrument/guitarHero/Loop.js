(function($, THREE, window, document, undefined) {	
	var Loop = function(){
/*/////////////////////////////////////////////////////////////////////////////
	Private Properties
/*/////////////////////////////////////////////////////////////////////////////
		var that;
/*/////////////////////////////////////////////////////////////////////////////
	Private Methods
/*/////////////////////////////////////////////////////////////////////////////
		var loop = function(){
			requestAnimationFrame(loop.bind(this));
			that.triggerTick();
		}
/*/////////////////////////////////////////////////////////////////////////////
	Public Methods
/*/////////////////////////////////////////////////////////////////////////////
		this.construct = function(){
			that=this;
			loop();
		}
	}
///////////////////////////////////////////////////////////////////////////////
	module.exports = require("../Class.js")(Loop).extend(require("./ticker.js"));
})(jQuery, THREE, window, document)